namespace Thelia\Core\Event\Content;

/**
 * Class ContentCreateEvent
 * @package Thelia\Core\Event\Content
 * @author manuel raynaud <mraynaud@openstudio.fr>
 */
class ContentCreateEvent extends ContentEvent
{
    protected $title;
    protected $default_folder;
    protected $locale;
    protected $visible;

    /**
     * @param mixed $locale
     *
     * @return $this
     */
    public function setLocale($locale)
    {
        $this->locale = $locale;

        return $this;
    }

    /**
     * @return mixed
     */
    public function getLocale()
    {
        return $this->locale;
    }

    /**
     * @param mixed $default_folder
     *
     * @return $this
     */
    public function setDefaultFolder($default_folder)
    {
        $this->default_folder = $default_folder;

        return $this;
    }

    /**
     * @return mixed
     */
    public function getDefaultFolder()
    {
        return $this->default_folder;
    }

    /**
     * @param mixed $visible
     *
     * @return $this
     */
    public function setVisible($visible)
    {
        $this->visible = $visible;

        return $this;
    }

    /**
     * @return mixed
     */
    public function getVisible()
    {
        return $this->visible;
    }

    /**
     * @param mixed $title
     *
     * @return $this
     */
    public function setTitle($title)
    {
        $this->title = $title;

        return $this;
    }

    /**
     * @return mixed
     */
    public function getTitle()
    {
        return $this->title;
    }

}
